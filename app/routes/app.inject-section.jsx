export const loader = async ({ request }) => {
  const { admin } = await authenticate.admin(request);
  
  try {
    // Get shop and theme info
    const shopRes = await admin.graphql(`query { shop { myshopifyDomain } }`);
    const shopJson = await shopRes.json();
    const shopDomain = shopJson.data.shop.myshopifyDomain;
    
    const themeRes = await admin.graphql(`query { themes(first: 10) { nodes { id name role } } }`);
    const themeJson = await themeRes.json();
    const themes = themeJson.data.themes.nodes;
    
    console.log("üîç Available themes:", themes);
    
    return json({ 
      shopDomain, 
      themes,
      mainTheme: themes.find(t => t.role === "MAIN"),
      publishedTheme: themes.find(t => t.role === "PUBLISHED")
    });
  } catch (error) {
    console.error("‚ùå Error in loader:", error);
    return json({ 
      shopDomain: "", 
      themes: [],
      error: error.message 
    });
  }
};

export const action = async ({ request }) => {
  const { admin } = await authenticate.admin(request);
  const formData = await request.formData();
  const actionType = formData.get("actionType");
  
  console.log("üöÄ Action triggered:", actionType);
  
  if (actionType === "inject_section") {
    const sectionType = formData.get("sectionType");
    const sectionName = formData.get("sectionName");
    const themeId = formData.get("themeId");
    
    console.log("üìù Injecting section:", { sectionType, sectionName, themeId });
    
    try {
      // Generate the section content based on type
      const sectionContent = generateSectionContent(sectionType, sectionName);
      console.log("üìÑ Generated section content length:", sectionContent.length);
      
      // Create the section file in the theme using GraphQL mutation
      const mutation = `
        mutation themeFilesUpsert($themeId: ID!, $files: [OnlineStoreThemeFilesUpsertFileInput!]!) {
          themeFilesUpsert(themeId: $themeId, files: $files) {
            job {
              done
              id
            }
            upsertedThemeFiles {
              filename
            }
            userErrors {
              field
              message
              code
            }
          }
        }
      `;
      
      const variables = {
        themeId: themeId,
        files: [{
          filename: `sections/${sectionName}.liquid`,
          body: {
            type: "TEXT",
            value: sectionContent
          }
        }]
      };
      
      console.log("üé® Using GraphQL mutation with variables:", JSON.stringify(variables, null, 2));
      
      const response = await admin.graphql(mutation, { variables });
      const result = await response.json();
      
      console.log("üìä GraphQL response:", JSON.stringify(result, null, 2));
      
      if (result.data?.themeFilesUpsert?.userErrors?.length > 0) {
        const error = result.data.themeFilesUpsert.userErrors[0];
        console.error("‚ùå GraphQL error:", error);
        return json({ 
          success: false, 
          error: `Failed to inject section: ${error.message}`,
          code: error.code
        });
      }
      
      if (result.errors) {
        console.error("‚ùå GraphQL errors:", result.errors);
        return json({ 
          success: false, 
          error: `GraphQL error: ${result.errors[0].message}`,
          details: result.errors
        });
      }
      
      console.log("‚úÖ Section created successfully:", result.data?.themeFilesUpsert?.upsertedThemeFiles);
      
      return json({ 
        success: true, 
        message: `Section ${sectionName} successfully added to your theme!`,
        sectionName,
        themeId: themeId.replace("gid://shopify/OnlineStoreTheme/", ""),
        files: result.data?.themeFilesUpsert?.upsertedThemeFiles?.map(file => file.filename)
      });
      
    } catch (error) {
      console.error("‚ùå Error injecting section:", error);
      return json({ 
        success: false, 
        error: `Failed to inject section: ${error.message}`,
        details: error.stack
      });
    }
  }
  
  return json({ success: false, error: "Invalid action" });
};

